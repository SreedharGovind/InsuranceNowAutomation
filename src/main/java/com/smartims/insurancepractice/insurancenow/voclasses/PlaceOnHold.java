package com.smartims.insurancepractice.insurancenow.voclasses;

public class PlaceOnHold {
    String placeOnHoldStartTransaction;
    String placeOnHoldType;
    String placeOnHoldDescription;
    String placeOnHoldReason;
    String placeOnHoldAutomaticallyReleaseHold;
    String placeOnHoldRemindMe;

    public String getPlaceOnHoldStartTransaction() {
        return placeOnHoldStartTransaction;
    }

    public void setPlaceOnHoldStartTransaction(String placeOnHoldStartTransaction) {
        this.placeOnHoldStartTransaction = placeOnHoldStartTransaction;
    }

    public String getPlaceOnHoldType() {
        return placeOnHoldType;
    }

    public void setPlaceOnHoldType(String placeOnHoldType) {
        this.placeOnHoldType = placeOnHoldType;
    }

    public String getPlaceOnHoldDescription() {
        return placeOnHoldDescription;
    }

    public void setPlaceOnHoldDescription(String placeOnHoldDescription) {
        this.placeOnHoldDescription = placeOnHoldDescription;
    }

    public String getPlaceOnHoldReason() {
        return placeOnHoldReason;
    }

    public void setPlaceOnHoldReason(String placeOnHoldReason) {
        this.placeOnHoldReason = placeOnHoldReason;
    }

    public String getPlaceOnHoldAutomaticallyReleaseHold() {
        return placeOnHoldAutomaticallyReleaseHold;
    }

    public void setPlaceOnHoldAutomaticallyReleaseHold(String placeOnHoldAutomaticallyReleaseHold) {
        this.placeOnHoldAutomaticallyReleaseHold = placeOnHoldAutomaticallyReleaseHold;
    }

    public String getPlaceOnHoldRemindMe() {
        return placeOnHoldRemindMe;
    }

    public void setPlaceOnHoldRemindMe(String placeOnHoldRemindMe) {
        this.placeOnHoldRemindMe = placeOnHoldRemindMe;
    }
}
