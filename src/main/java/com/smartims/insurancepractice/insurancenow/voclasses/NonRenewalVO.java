package com.smartims.insurancepractice.insurancenow.voclasses;

public class NonRenewalVO {
    String nonRenewal;
    String nonRenewalReason;
    String nonRenewalDescription;
    String nonRenewalAdditionalNoticeText;

    public String getNonRenewal() {
        return nonRenewal;
    }

    public void setNonRenewal(String nonRenewal) {
        this.nonRenewal = nonRenewal;
    }

    public String getNonRenewalReason() {
        return nonRenewalReason;
    }

    public void setNonRenewalReason(String nonRenewalReason) {
        this.nonRenewalReason = nonRenewalReason;
    }

    public String getNonRenewalDescription() {
        return nonRenewalDescription;
    }

    public void setNonRenewalDescription(String nonRenewalDescription) {
        this.nonRenewalDescription = nonRenewalDescription;
    }

    public String getNonRenewalAdditionalNoticeText() {
        return nonRenewalAdditionalNoticeText;
    }

    public void setNonRenewalAdditionalNoticeText(String nonRenewalAdditionalNoticeText) {
        this.nonRenewalAdditionalNoticeText = nonRenewalAdditionalNoticeText;
    }
}
