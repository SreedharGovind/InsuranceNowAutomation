package com.smartims.insurancepractice.insurancenow.voClasses;

public class CancellationVO {
    String cancellationStartTransaction;
    String cancellationNoticeDate;
    String cancellationCancellationType;
    String cancellationReason;
    String cancellationEffectiveDate;
    String cancellationType;
    String cancellationDescription;
    String cancellationAdditionalNoticeText;

    public String getCancellationStartTransaction() {
        return cancellationStartTransaction;
    }

    public void setCancellationStartTransaction(String cancellationStartTransaction) {
        this.cancellationStartTransaction = cancellationStartTransaction;
    }

    public String getCancellationNoticeDate() {
        return cancellationNoticeDate;
    }

    public void setCancellationNoticeDate(String cancellationNoticeDate) {
        this.cancellationNoticeDate = cancellationNoticeDate;
    }

    public String getCancellationCancellationType() {
        return cancellationCancellationType;
    }

    public void setCancellationCancellationType(String cancellationCancellationType) {
        this.cancellationCancellationType = cancellationCancellationType;
    }

    public String getCancellationReason() {
        return cancellationReason;
    }

    public void setCancellationReason(String cancellationReason) {
        this.cancellationReason = cancellationReason;
    }

    public String getCancellationEffectiveDate() {
        return cancellationEffectiveDate;
    }

    public void setCancellationEffectiveDate(String cancellationEffectiveDate) {
        this.cancellationEffectiveDate = cancellationEffectiveDate;
    }

    public String getCancellationType() {
        return cancellationType;
    }

    public void setCancellationType(String cancellationType) {
        this.cancellationType = cancellationType;
    }

    public String getCancellationDescription() {
        return cancellationDescription;
    }

    public void setCancellationDescription(String cancellationDescription) {
        this.cancellationDescription = cancellationDescription;
    }

    public String getCancellationAdditionalNoticeText() {
        return cancellationAdditionalNoticeText;
    }

    public void setCancellationAdditionalNoticeText(String cancellationAdditionalNoticeText) {
        this.cancellationAdditionalNoticeText = cancellationAdditionalNoticeText;
    }
}
